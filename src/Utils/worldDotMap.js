export function geoToGrid(lon, lat) {
  // Equirectangular projection matching the Mistral SVG
  const col = (lon + 180) / 360 * 134;
  const row = (90 - lat) / 180 * 69;
  return {
    x: col * DOT_SPACING + DOT_OFFSET_X,
    y: row * DOT_SPACING + DOT_OFFSET_Y
  };
}

